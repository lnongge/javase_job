package com.itheima.job.day01;

public class MathDemo {
	public static void main(String[] args) {
		System.out.println(Math.PI);//π
		System.out.println(Math.abs(-10));//取绝对值
		System.out.println(Math.ceil(2.3));//天花板,向上取整
		System.out.println(Math.floor(1.2));//地板,向下取整
		System.out.println(Math.round(2.3));//四舍五入
		System.out.println(Math.pow(2, 3));//求2的3次幂
		System.out.println(Math.random());//生成一个0-1之间的随机数
	}
}
